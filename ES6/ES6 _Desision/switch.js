var grade = "F";
switch(grade)
{
    case "A":
        {
            console.log("Exellent")
            break
        }
    case "B":
        {
            console.log("Good")
            break
        }
    case "C":
        {
            console.log("Fair")
            break
        }
    case "D":
        {
            console.log("Poor")
            break
        }
    default:
    {
        console.log("invalid grade code")
        break
    }
}
